/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();


  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();

  /* Define Local Variables */
  float temperature = 0.0;
  uint32_t ADCReading;

  /* Reset LED */
  ledOff();

  /* Start ADC Conversion */
  HAL_ADC_Start(&hadc1);


  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  /* Poll ADC */
	  HAL_ADC_PollForConversion(&hadc1, 100);	// Poll ADC
	  ADCReading = HAL_ADC_GetValue(&hadc1);	// Record ADC Reading
	  temperature = ((float) ADCReading) * 3300.0 / 4095.0; // Converting to mV
	  temperature = (temperature) / 10.0;	// Convert to degree C from mV (LM34 & LM35)

	  /* Check if breaks LED threshold */
	  if (temperature > THRESHOLD) {
		  ledOn();
	  } else {
		  ledOff();
	  }

	  /* 10 mSecond Delay */
	  HAL_Delay(10);
  }
  /* USER CODE END 3 */
}

